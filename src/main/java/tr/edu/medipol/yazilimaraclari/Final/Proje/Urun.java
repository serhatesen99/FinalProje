package tr.edu.medipol.yazilimaraclari.Final.Proje;

import java.util.Date;


public class Urun {
    private Long id;
    private String ad;
    private double fiyat;
    private Date skt;

    
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    
    
    
    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    
    
    
    public double getFiyat() {
        return fiyat;
    }

    public void setFiyat(double fiyat) {
        this.fiyat = fiyat;
    }

    
    
    
    
    public Date getSkt() {
        return skt;
    }

    public void setSkt(Date skt) {
        this.skt = skt;
    }
    
    
    
}



    